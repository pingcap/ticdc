package metrics

import "github.com/prometheus/client_golang/prometheus"

const (
	namespace = "ticdc"
	subsystem = "sink"
)

// Metrics for cloud storage sink
var (
	// CloudStorageWriteBytesGauge records the total number of bytes written to cloud storage.
	CloudStorageWriteBytesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_write_bytes_total",
		Help:      "Total number of bytes written to cloud storage",
	}, []string{"namespace", "changefeed"})

	// CloudStorageFileCountGauge records the number of files generated by cloud storage sink.
	CloudStorageFileCountGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_file_count",
		Help:      "Total number of files managed by a cloud storage sink",
	}, []string{"namespace", "changefeed"})

	// CloudStorageWriteDurationHistogram records the latency distributions of writeLog.
	CloudStorageWriteDurationHistogram = prometheus.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_write_duration_seconds",
		Help:      "The latency distributions of write storage by a cloud storage sink",
		Buckets:   prometheus.ExponentialBuckets(0.001, 2.0, 13),
	}, []string{"namespace", "changefeed"})

	// CloudStorageFlushDurationHistogram records the latency distributions of flushLog.
	CloudStorageFlushDurationHistogram = prometheus.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_flush_duration_seconds",
		Help:      "The latency distributions of flush storage by a cloud storage sink",
		Buckets:   prometheus.ExponentialBuckets(0.001, 2.0, 13),
	}, []string{"namespace", "changefeed"})

	CloudStorageDDLFlushDurationHistogram = prometheus.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_ddl_flush_duration_seconds",
		Help:      "DDL flush duration for cloud storage sink",
		Buckets:   prometheus.ExponentialBuckets(0.001, 2.0, 13),
	}, []string{"namespace", "changefeed"})

	// CloudStorageWorkerBusyRatio records the busy ratio of CloudStorage bgUpdateLog worker.
	CloudStorageWorkerBusyRatio = prometheus.NewCounterVec(
		prometheus.CounterOpts{
			Namespace: namespace,
			Subsystem: subsystem,
			Name:      "cloud_storage_worker_busy_ratio",
			Help:      "Busy ratio for cloud storage sink dml worker.",
		}, []string{"namespace", "changefeed", "id"})

	CloudStorageSpoolMemoryBytesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_spool_memory_bytes",
		Help:      "Current memory bytes used by cloud storage spool",
	}, []string{"namespace", "changefeed"})

	CloudStorageSpoolDiskBytesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_spool_disk_bytes",
		Help:      "Current disk bytes used by cloud storage spool",
	}, []string{"namespace", "changefeed"})

	CloudStoragePendingPostEnqueueCounter = prometheus.NewCounterVec(prometheus.CounterOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_pending_post_enqueue_total",
		Help:      "Total number of PostEnqueue callbacks moved into the pending queue in cloud storage sink",
	}, []string{"namespace", "changefeed"})

	CloudStoragePendingPostEnqueueGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_pending_post_enqueue",
		Help:      "Current number of PostEnqueue callbacks waiting in cloud storage spool",
	}, []string{"namespace", "changefeed"})

	CloudStoragePostEnqueuePausedGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_post_enqueue_paused",
		Help:      "Whether cloud storage spool is currently pausing PostEnqueue callbacks",
	}, []string{"namespace", "changefeed"})

	CloudStorageSpillBytesHistogram = prometheus.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_spill_bytes",
		Help:      "Byte distribution of encoded batches spilled into cloud storage spool files",
		Buckets:   prometheus.ExponentialBuckets(1024, 2.0, 20),
	}, []string{"namespace", "changefeed"})

	CloudStorageLoadBytesHistogram = prometheus.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_load_bytes",
		Help:      "Byte distribution of encoded batches loaded from cloud storage spool",
		Buckets:   prometheus.ExponentialBuckets(1024, 2.0, 20),
	}, []string{"namespace", "changefeed"})

	CloudStorageRotateCountCounter = prometheus.NewCounterVec(prometheus.CounterOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_rotate_total",
		Help:      "Total number of local spool segment rotations in cloud storage sink",
	}, []string{"namespace", "changefeed"})

	CloudStorageSpoolSegmentCountGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_spool_segment_count",
		Help:      "Current number of live cloud storage spool segments",
	}, []string{"namespace", "changefeed"})

	CloudStorageSpoolStageErrorCounter = prometheus.NewCounterVec(prometheus.CounterOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_spool_stage_error_total",
		Help:      "Total number of cloud storage spool errors by stage",
	}, []string{"namespace", "changefeed", "stage"})

	CloudStoragePendingTablesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_pending_tables",
		Help:      "Current number of tables buffered in one cloud storage writer shard",
	}, []string{"namespace", "changefeed", "writer"})

	CloudStoragePendingEntriesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_pending_entries",
		Help:      "Current number of buffered entries in one cloud storage writer shard",
	}, []string{"namespace", "changefeed", "writer"})

	CloudStoragePendingBytesGauge = prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_pending_bytes",
		Help:      "Current buffered bytes in one cloud storage writer shard before flush",
	}, []string{"namespace", "changefeed", "writer"})

	CloudStorageFlushCountCounter = prometheus.NewCounterVec(prometheus.CounterOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_flush_total",
		Help:      "Total number of flush batches emitted by one cloud storage writer shard",
	}, []string{"namespace", "changefeed", "writer", "reason"})

	CloudStorageWriterErrorCounter = prometheus.NewCounterVec(prometheus.CounterOpts{
		Namespace: namespace,
		Subsystem: subsystem,
		Name:      "cloud_storage_writer_error_total",
		Help:      "Total number of cloud storage writer errors by stage",
	}, []string{"namespace", "changefeed", "writer", "stage"})
)

// InitCloudStorageMetrics registers all metrics in this file.
func InitCloudStorageMetrics(registry *prometheus.Registry) {
	registry.MustRegister(CloudStorageWriteBytesGauge)
	registry.MustRegister(CloudStorageFileCountGauge)
	registry.MustRegister(CloudStorageWriteDurationHistogram)
	registry.MustRegister(CloudStorageFlushDurationHistogram)
	registry.MustRegister(CloudStorageDDLFlushDurationHistogram)
	registry.MustRegister(CloudStorageWorkerBusyRatio)
	registry.MustRegister(CloudStorageSpoolMemoryBytesGauge)
	registry.MustRegister(CloudStorageSpoolDiskBytesGauge)
	registry.MustRegister(CloudStoragePendingPostEnqueueCounter)
	registry.MustRegister(CloudStoragePendingPostEnqueueGauge)
	registry.MustRegister(CloudStoragePostEnqueuePausedGauge)
	registry.MustRegister(CloudStorageSpillBytesHistogram)
	registry.MustRegister(CloudStorageLoadBytesHistogram)
	registry.MustRegister(CloudStorageRotateCountCounter)
	registry.MustRegister(CloudStorageSpoolSegmentCountGauge)
	registry.MustRegister(CloudStorageSpoolStageErrorCounter)
	registry.MustRegister(CloudStoragePendingTablesGauge)
	registry.MustRegister(CloudStoragePendingEntriesGauge)
	registry.MustRegister(CloudStoragePendingBytesGauge)
	registry.MustRegister(CloudStorageFlushCountCounter)
	registry.MustRegister(CloudStorageWriterErrorCounter)
}
